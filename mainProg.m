    obj=get(handles.edit4);
else
    obj=handles.edit4;
end;
subInfo.age=obj.String;
if(isnumeric(handles.popupmenu1)) % This is Matlab 2013, handels are numeric!
    obj=get(handles.popupmenu1);
else
    obj=handles.popupmenu1;
end;
subInfo.gender= obj.String{obj.Value};
if(isnumeric(handles.popupmenu2)) % This is Matlab 2013, handels are numeric!
    obj=get(handles.popupmenu2);
else
    obj=handles.popupmenu2;
end;
subInfo.session= obj.String{obj.Value};
handles.pushbutton1.Enable='off';
pause(1);
runExperiment(subInfo); % Now run the experiment
handles.pushbutton1.Enable='on';

